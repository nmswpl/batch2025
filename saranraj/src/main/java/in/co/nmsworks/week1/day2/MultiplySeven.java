package in.co.nmsworks.week1.day2;

public class MultiplySeven {
    public static void main(String[] args) {
        seven();

    }

    public static void seven() {
        System.out.println("Multiplication of seven table");
        for(int i=1;i<=10;i++){
            System.out.println("7 X"+" "+i+" = "+7*i);
        }
    }
}
