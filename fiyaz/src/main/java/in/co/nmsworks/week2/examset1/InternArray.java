package in.co.nmsworks.week2.examset1;

public class InternArray {

    private String name;
    private String dept;

    public InternArray() {

    }

    public InternArray(String name, String dept) {
        this.name = name;
        this.dept = dept;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }
}
